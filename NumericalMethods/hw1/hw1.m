% f(x) = (x - 1)(x - 2)(x - 3)(x - 4) = x^4 - 10x^3 + 35x^2 - 50x + 24
% g1(x) = x - 0.8f(x)
% g2(x) = x + 1.1f(x)
% g3(x) = (x^4 - 10x^3 + 35x^2 + 24) / 50
% g4(x) = sqrt[(-x^4 + 10x^3 + 50x - 24) / 35]
% g5(x) = x - f(x) / fp(x)
% fp(x) = 4x^3 - 30x^2 + 70x - 50
digits(5)
x = [0.99; 1.01; 1.99; 2.01; 2.99; 3.01; 3.99; 4.01; 0.5; 1.5; 2.5; 3.5];
gx = [x,x,x,x,x];
for i = 1:200
    fx = (gx - 1) .* (gx - 2) .* (gx - 3) .* (gx - 4);
    g1_n = gx(:, 1) - 0.8 * fx(:, 1);
    g2_n = gx(:, 2) + 1.1 * fx(:, 2);
    g3_n = (gx(:, 3).^4 - 10*gx(:, 3).^3 + 35*gx(:, 3).^2 + 24) ./ 50;
    g4_n = ((-gx(:, 4).^4 + 10*gx(:, 4).^3 + 50*gx(:, 4) - 24) ./ 35).^0.5;
    fp_n = 4*gx(:, 5).^3 - 30*gx(:, 5).^2 + 70*gx(:, 5) - 50;
    g5_n = gx(:, 5) - fx(:, 5) ./ fp_n;
    gx = [g1_n, g2_n, g3_n, g4_n, g5_n];
end;
gx = real(gx)


    
    